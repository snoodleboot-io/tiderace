#!/usr/bin/env python3
"""Generate the deterministic fixture-heavy conformance corpus ``fx_corpus``.

This corpus is the shared target for the Phase 3 (Fixtures + Watermarks) ATDD,
TDD, and perf lanes. It is a set of *real pytest files* that pass under
``pytest -q`` and that exercise every fixture feature the pure-Rust engine must
reimplement natively (no pytest underneath, per ADR-E001):

    * session / package / module / class / function scoped fixtures
    * an ``autouse`` fixture (declared in a conftest.py)
    * override-by-location (a module-level fixture shadows a same-named
      session/conftest fixture; nearer wins)
    * yield-style teardown / finalizers with OBSERVABLE ordering (every setup
      and teardown appends to a shared event log file, so reverse-order
      teardown is checkable by the Rust differential oracle)
    * parametrized fixtures (``params=[...]``)
    * a NON-FORK-SAFE resource: a fixture that opens ``sqlite3.connect(":memory:")``.
      Intent is declared to the Rust side two ways the engine can detect:
        - the fixture name is prefixed ``reinit_after_fork__`` (naming convention)
        - the test that uses it is tagged ``@pytest.mark.reinit_after_fork``
    * a scope-count probe: every fixture body increments a counter in a shared
      JSON-ish counts file, so it is verifiable that a module/session fixture
      body runs ONCE while function fixtures run per-test
    * a module of ~500 trivial tests sharing a single module fixture (the
      "1x not 500x" claim target)

Determinism: the generated content contains NO timestamps and NO randomness, so
the tree can be committed and regenerated byte-for-byte identically. Runtime
artifacts (the event log + counts file) are written under a per-run temp dir
chosen via a session fixture, NOT into the committed tree.

Usage:
    python generate_fx_corpus.py [--big-tests N] [--param-values A B C] [--out DIR]

Defaults (--big-tests 500) collect 525 tests total. The exact collected count is
printed at the end and recorded in env-manifest.md.
"""
from __future__ import annotations

import argparse
import shutil
from pathlib import Path

# ---------------------------------------------------------------------------
# Shared instrumentation helpers (written verbatim into the corpus).
# Every fixture records (a) an ordered event line in EVENT log and (b) a bump
# in the COUNTS file. The Rust differential oracle reads these to assert setup
# order, reverse-teardown order, and per-scope run counts.
# ---------------------------------------------------------------------------
PROBE_SRC = '''\
"""Deterministic instrumentation shared by the fx_corpus fixtures.

No randomness, no timestamps: every value is derived from explicit arguments so
the corpus is regenerable byte-for-byte and the recorded events are stable
across runs (modulo pytest's own collection order, which is itself stable).

Two artifacts are written into a per-run directory (provided by the
``probe_dir`` session fixture, NOT the committed tree):

    events.log  - one line per setup/teardown, in execution order
    counts.json - {fixture_name: times_its_body_ran}

The Rust side asserts: (1) teardown order == reverse(setup order) per scope,
(2) wider-scope fixture bodies ran once while function fixtures ran per test.
"""
import json
import os
from pathlib import Path

ENV_DIR = "FX_CORPUS_PROBE_DIR"


def _dir() -> Path:
    d = os.environ.get(ENV_DIR)
    if not d:
        raise RuntimeError(
            "FX_CORPUS_PROBE_DIR not set; the probe_dir session fixture must "
            "run first"
        )
    p = Path(d)
    p.mkdir(parents=True, exist_ok=True)
    return p


def record_event(line: str) -> None:
    """Append a single ordered event line to events.log."""
    with (_dir() / "events.log").open("a", encoding="utf-8") as fh:
        fh.write(line + "\\n")


def bump_count(name: str) -> int:
    """Increment and return the run-count for a fixture body."""
    path = _dir() / "counts.json"
    counts = {}
    if path.exists():
        counts = json.loads(path.read_text(encoding="utf-8"))
    counts[name] = counts.get(name, 0) + 1
    path.write_text(json.dumps(counts, sort_keys=True), encoding="utf-8")
    return counts[name]


def setup(name: str) -> None:
    record_event("SETUP   " + name)
    bump_count(name)


def teardown(name: str) -> None:
    record_event("TEARDOWN " + name)
'''

# ---------------------------------------------------------------------------
# Root conftest.py: probe_dir (session), session-scoped fixtures, a session
# autouse fixture, and a session fixture named ``shared_value`` that a module
# below will OVERRIDE by location.
# ---------------------------------------------------------------------------
ROOT_CONFTEST_SRC = '''\
"""Root (session) conftest for fx_corpus.

Provides the per-run probe directory, the session-scoped fixtures, a
session-scoped AUTOUSE fixture (enters every test's closure without being
requested), and the session-level ``shared_value`` fixture that a leaf module
overrides by location (nearer-wins).
"""
import os
import sys
import tempfile

import pytest

sys.path.insert(0, os.path.dirname(__file__))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture(scope="session")
def probe_dir():
    """Create a fresh per-run probe dir and expose it via env var.

    Yields the path; cleans up on session teardown. This is the ONLY fixture
    allowed to touch the filesystem outside the committed tree, keeping the
    corpus deterministic.
    """
    # Honor a caller-supplied dir (the Rust oracle / a debugging run sets
    # FX_CORPUS_PROBE_DIR to inspect events.log + counts.json); otherwise make a
    # fresh temp dir. Either way the value is exported so fx_probe can find it.
    d = os.environ.get("FX_CORPUS_PROBE_DIR") or tempfile.mkdtemp(
        prefix="fx_corpus_probe_"
    )
    os.makedirs(d, exist_ok=True)
    os.environ["FX_CORPUS_PROBE_DIR"] = d
    setup("probe_dir")
    yield d
    teardown("probe_dir")


@pytest.fixture(scope="session")
def session_db(probe_dir):
    """Session-scoped fixture: set up ONCE in the wellspring lineage.

    Stands in for an expensive session resource (Layer 2 / Watermark S). The
    scope-count probe must show this body ran exactly once across the suite.
    """
    setup("session_db")
    yield {"rows": 10_000}
    teardown("session_db")


@pytest.fixture(scope="session", autouse=True)
def session_autouse(probe_dir):
    """Session AUTOUSE fixture: injected into every in-scope test's closure
    without being requested by name."""
    setup("session_autouse")
    yield
    teardown("session_autouse")


@pytest.fixture(scope="session")
def shared_value(probe_dir):
    """Session definition of ``shared_value`` — OVERRIDDEN by location in
    pkg_override/test_override.py (module def must win there)."""
    setup("shared_value@session")
    yield "from-session-conftest"
    teardown("shared_value@session")
'''


def pkg_init() -> str:
    return ""


# ---------------------------------------------------------------------------
# tests/ package conftest: PACKAGE-scoped fixtures (Layer 2.5, between Session
# and Module). Visible to every module in the tests package. Set up once per
# package path.
# ---------------------------------------------------------------------------
PKG_CONFTEST_SRC = '''\
"""Package conftest for the tests package: a package-scoped fixture (Layer 2.5,
between Session and Module) plus a package-scoped autouse fixture. Set up once
per package path."""
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture(scope="package")
def pkg_resource(session_db):
    """Package-scoped: depends on the wider session_db (scope-monotone:
    package may depend on session)."""
    setup("pkg_resource")
    yield {"pkg": True, "rows": session_db["rows"]}
    teardown("pkg_resource")


@pytest.fixture(scope="package", autouse=True)
def pkg_autouse():
    """A second autouse fixture, at package scope, to exercise multi-level
    autouse injection."""
    setup("pkg_autouse")
    yield
    teardown("pkg_autouse")
'''

# Module exercising module/class/function scopes + yield ordering + autouse.
SCOPES_MODULE_SRC = '''\
"""Exercise module / class / function scopes with observable yield-teardown
ordering. Setup order (per class test) is:
    session_db (once) -> pkg_resource (once) -> module_fix (once)
      -> class_fix (once per class) -> func_fix (per test)
Teardown must be the strict reverse at each scope boundary.
"""
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture(scope="module")
def module_fix(pkg_resource):
    """Module-scoped: body runs ONCE for this module regardless of test count."""
    setup("module_fix")
    yield {"module": True}
    teardown("module_fix")


@pytest.fixture(scope="class")
def class_fix(module_fix):
    """Class-scoped: body runs once per class."""
    setup("class_fix")
    yield {"class": True}
    teardown("class_fix")


@pytest.fixture
def func_fix(class_fix):
    """Function-scoped (default): body runs per test, torn down per test."""
    setup("func_fix")
    yield {"func": True}
    teardown("func_fix")


class TestScoped:
    """Two tests in one class share class_fix (once) but get fresh func_fix."""

    def test_a(self, func_fix):
        assert func_fix["func"] is True

    def test_b(self, func_fix):
        assert func_fix["func"] is True


class TestScopedAgain:
    """A second class -> class_fix body runs a second time, module_fix does not."""

    def test_c(self, func_fix):
        assert func_fix["func"] is True
'''

# Parametrized fixture module.
PARAM_MODULE_SRC = '''\
"""Parametrized fixture: params=[...] fans out into one instance per value.
Each value drives a distinct test invocation (and, on the Rust side, a distinct
closure_hash)."""
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402

PARAM_VALUES = {param_values!r}


@pytest.fixture(params=PARAM_VALUES)
def parametrized(request):
    """Fans out into len(PARAM_VALUES) instances; body runs once per param."""
    value = request.param
    setup("parametrized[%s]" % value)
    yield value
    teardown("parametrized[%s]" % value)


def test_param(parametrized):
    assert parametrized in PARAM_VALUES
'''

# Override-by-location module: defines its own ``shared_value`` (module wins).
OVERRIDE_MODULE_SRC = '''\
"""Override-by-location: this module defines ``shared_value`` at module scope,
shadowing the session-conftest definition of the same name. Nearer wins, so the
test here must resolve to the module value, never the conftest value."""
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture
def shared_value():
    """Module-level override of the session-conftest ``shared_value``."""
    setup("shared_value@module")
    yield "from-module-override"
    teardown("shared_value@module")


def test_override_wins(shared_value):
    # If override-by-location is correct, the MODULE definition wins here.
    assert shared_value == "from-module-override"
'''

# Non-fork-safe sqlite resource module.
SQLITE_MODULE_SRC = '''\
"""Non-fork-safe resource boundary (reinit_after_fork).

``sqlite3.connect(":memory:")`` yields a connection that does NOT survive fork()
intact: a forked child must open a FRESH connection, never reuse the parent's
inherited handle. Intent is declared two ways the Rust engine can detect:

  1. Naming convention: the fixture name is prefixed ``reinit_after_fork__``.
  2. Marker: tests using it carry ``@pytest.mark.reinit_after_fork``.

Under plain pytest (no fork) this simply passes; the Rust differential oracle
uses the markers to assert each forked child gets a distinct connection.
"""
import os
import sqlite3
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture
def reinit_after_fork__db_conn():
    """A non-fork-safe in-memory sqlite connection.

    Declared scope is function here, but the engine treats the connection HANDLE
    as reinit-in-child regardless of declared scope (split-setup). The
    ``reinit_after_fork__`` name prefix is the detectable intent declaration.
    """
    setup("reinit_after_fork__db_conn")
    conn = sqlite3.connect(":memory:")
    conn.execute("CREATE TABLE t (id INTEGER PRIMARY KEY, v TEXT)")
    conn.execute("INSERT INTO t (v) VALUES ('alpha'), ('beta')")
    conn.commit()
    yield conn
    conn.close()
    teardown("reinit_after_fork__db_conn")


@pytest.mark.reinit_after_fork
def test_sqlite_resource(reinit_after_fork__db_conn):
    rows = reinit_after_fork__db_conn.execute(
        "SELECT v FROM t ORDER BY id"
    ).fetchall()
    assert [r[0] for r in rows] == ["alpha", "beta"]


@pytest.mark.reinit_after_fork
def test_sqlite_fresh_per_test(reinit_after_fork__db_conn):
    # A second test re-acquires a fresh connection with the same seed data.
    count = reinit_after_fork__db_conn.execute("SELECT COUNT(*) FROM t").fetchone()
    assert count[0] == 2
'''

# C-extension (numpy) module — exercises fork-from-warm against a real native stack.
NUMPY_MODULE_SRC = '''\
"""Real C-extension boundary: a session-scoped fixture imports and uses numpy,
so the wellspring snapshot is taken with a real native extension already warm in
memory (the E-2 fork-from-warm hazard). Under plain pytest this just passes."""
import os
import sys

import numpy as np
import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture(scope="module")
def warm_array(session_db):
    """Module-scoped numpy array built once; snapshotted with numpy warm."""
    setup("warm_array")
    arr = np.arange(1000, dtype=np.int64)
    yield arr
    teardown("warm_array")


def test_numpy_sum(warm_array):
    assert int(warm_array.sum()) == (999 * 1000) // 2


def test_numpy_shape(warm_array):
    assert warm_array.shape == (1000,)
'''


def big_module_src(n: int) -> str:
    """~N trivial tests sharing ONE module fixture (the 1x-not-Nx claim)."""
    tests = []
    for t in range(n):
        tests.append(
            f"def test_big_{t}(big_module_fix):\n"
            f"    assert big_module_fix['n'] == {n}\n"
        )
    body = "\n\n".join(tests)
    return f'''\
"""{n} trivial tests sharing a single module-scoped fixture.

The module fixture body must run ONCE (not {n} times) — verified via the
scope-count probe (counts.json["big_module_fix"] == 1). This is the
"1x not {n}x" performance claim target.
"""
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))
from fx_probe import setup, teardown  # noqa: E402


@pytest.fixture(scope="module")
def big_module_fix(session_db):
    setup("big_module_fix")
    yield {{"n": {n}}}
    teardown("big_module_fix")


{body}
'''


PYTEST_INI_SRC = '''\
[pytest]
testpaths = tests
markers =
    reinit_after_fork: fixture/resource is non-fork-safe; child must reinit it
'''


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--big-tests", type=int, default=500,
                    help="number of trivial tests sharing one module fixture")
    ap.add_argument("--param-values", nargs="+", default=["a", "b", "c"],
                    help="values for the parametrized fixture")
    ap.add_argument("--out", type=Path,
                    default=Path(__file__).parent)
    args = ap.parse_args()

    out: Path = args.out
    tests = out / "tests"
    pkg = tests / "pkg_override"

    # Clean only the generated children, never the generator itself.
    if tests.exists():
        shutil.rmtree(tests)
    for stale in ("conftest.py", "fx_probe.py", "pytest.ini", "__init__.py"):
        p = out / stale
        if p.exists():
            p.unlink()

    tests.mkdir(parents=True)
    pkg.mkdir(parents=True)

    # Top-level scaffolding.
    (out / "__init__.py").write_text(pkg_init())
    (out / "fx_probe.py").write_text(PROBE_SRC)
    (out / "conftest.py").write_text(ROOT_CONFTEST_SRC)
    (out / "pytest.ini").write_text(PYTEST_INI_SRC)

    # tests/ package. The package-scoped fixtures live in tests/conftest.py so
    # they are visible to every module in the package (Layer 2.5).
    (tests / "__init__.py").write_text(pkg_init())
    (tests / "conftest.py").write_text(PKG_CONFTEST_SRC)
    (tests / "test_scopes.py").write_text(SCOPES_MODULE_SRC)
    (tests / "test_param.py").write_text(
        PARAM_MODULE_SRC.format(param_values=list(args.param_values))
    )
    (tests / "test_sqlite_resource.py").write_text(SQLITE_MODULE_SRC)
    (tests / "test_numpy_cext.py").write_text(NUMPY_MODULE_SRC)
    (tests / "test_big_module.py").write_text(big_module_src(args.big_tests))

    # tests/pkg_override/ subpackage: override-by-location (module shadows the
    # session-conftest fixture of the same name).
    (pkg / "__init__.py").write_text(pkg_init())
    (pkg / "test_override.py").write_text(OVERRIDE_MODULE_SRC)

    # Expected collected count (deterministic):
    #   test_scopes:   3 (test_a, test_b, test_c)
    #   test_param:    len(param_values)
    #   test_sqlite:   2
    #   test_numpy:    2
    #   test_big:      big_tests
    #   test_override: 1
    n_param = len(args.param_values)
    total = 3 + n_param + 2 + 2 + args.big_tests + 1
    print(f"Generated {out}")
    print(f"  scopes module:   3 tests")
    print(f"  param module:    {n_param} tests (params={list(args.param_values)})")
    print(f"  sqlite module:   2 tests (reinit_after_fork)")
    print(f"  numpy module:    2 tests (C-ext)")
    print(f"  big module:      {args.big_tests} tests (share 1 module fixture)")
    print(f"  override module: 1 test (package scope + override-by-location)")
    print(f"  EXPECTED TOTAL:  {total} tests")


if __name__ == "__main__":
    main()
